package model;

public class DrugPhase {

    private int id;
    private String phaseTerm;


    public DrugPhase(){

    }


    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }


    public String getPhaseTerm() {
        return phaseTerm;
    }

    public void setPhaseTerm(String phaseTerm) {
        this.phaseTerm = phaseTerm;
    }
}
