package model;

public class DrugIntake {

    private int id;
    private String intakeTerm;


    public DrugIntake(){

    }


    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getIntakeTerm() {
        return intakeTerm;
    }

    public void setIntakeTerm(String intakeTerm) {
        this.intakeTerm = intakeTerm;
    }
}
